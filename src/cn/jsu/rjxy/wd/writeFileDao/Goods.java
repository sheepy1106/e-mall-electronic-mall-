public class Goods {
	private String GsID;
	private String GsName;
	private String GsPrice;
	private String GsNum;
	private String GsKind;

	public String getGsID() {
		return GsID;
	}

	public void setGsID(String gsID) {
		GsID = gsID;
	}

	public String getGsName() {
		return GsName;
	}

	public void setGsName(String gsName) {
		GsName = gsName;
	}

	public String getGsPrice() {
		return GsPrice;
	}

	public void setGsPrice(String gsPrice) {
		GsPrice = gsPrice;
	}

	public String getGsNum() {
		return GsNum;
	}

	public void setGsNum(String gsNum) {
		GsNum = gsNum;
	}

	public String getGsKind() {
		return GsKind;
	}

	public void setGsKind(String gsKind) {
		GsKind = gsKind;
	}

}
